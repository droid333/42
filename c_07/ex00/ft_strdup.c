#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	int		size;
	char	*ptr;

	size = 0;
	while (src[size])
		size++;
	ptr = (char *)malloc(sizeof(*ptr) * size + 1);
	if (!ptr)
		return (NULL);
	i = 0;
	while (src[i])
	{
		ptr[i] = src[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
